 private Color theCurrentMenuColor = new Color(39, 63, 109);
 private Color theSelectedMenuColor = new Color(100, 120, 170);
 private Color theTextMenuColor = new Color(255, 255, 255);
 private JLabel TheMenuLabelReference = new JLabel();
 private JLabel thePressedLabelReference = null;
 private JLabel theEnteredLabelReference = null;
 private boolean pressedSelected = false;
 private Icon arrowIcon = new javax.swing.ImageIcon(getClass().getResource("/images/arrow.gif"));
 private Icon arrowSelectedIcon = new javax.swing.ImageIcon(getClass().getResource("/images/arrow_selected.gif"));
 private Icon arrowMouseOverIcon = new javax.swing.ImageIcon(getClass().getResource("/images/arrow_mouseover.gif"));  
   
 private void setSelectedMenuItem(JLabel theMenuLabel)
  {
    try
    {
        TheMenuLabelReference.setBackground(theCurrentMenuColor);
        TheMenuLabelReference.setIcon(arrowIcon);
        theMenuLabel.setBackground(theSelectedMenuColor);
        theMenuLabel.setIcon(arrowSelectedIcon);
        TheMenuLabelReference = theMenuLabel;
    }
    catch(Exception e)
    {
        System.out.println(e);
    }
  }    
 
 private void setMouseEnteredMenuLabel(JLabel theEnteredLabel)
 {
    try
    {
        theEnteredLabelReference = theEnteredLabel;
        if(theEnteredLabel.equals(TheMenuLabelReference)==false)
        {
            theEnteredLabel.setIcon(arrowMouseOverIcon);
            if(pressedSelected==true)
            {
                
                TheMenuLabelReference.setForeground(theTextMenuColor);
            }
        }
        else
        {
            if(pressedSelected==true)
            {
                TheMenuLabelReference.setForeground(theSelectedMenuColor);    
            }
        }
    }
    catch(Exception e)
    {
        System.out.println(e);
    }     
 }
 private void setMouseExitedMenuLabel(JLabel theExitedLabel)
 {
    try
    {
        if(theExitedLabel.equals(TheMenuLabelReference)==false)
        {
            theExitedLabel.setIcon(arrowIcon);   
        }
        else
        {
            thePressedLabelReference.setForeground(theTextMenuColor);           
        }
    }
    catch(Exception e)
    {
        System.out.println(e);
    }  
 }

  private void setMousePressedMenuLabel(JLabel thePressedLabel)
 {
    try
    {
        thePressedLabelReference = thePressedLabel;
        if(thePressedLabel.equals(TheMenuLabelReference)==false)
        {
            thePressedLabel.setBackground(theSelectedMenuColor);           
        }
        else
        {
            pressedSelected=true; 
            thePressedLabel.setForeground(theSelectedMenuColor);
        }
    }
    catch(Exception e)
    {
        System.out.println(e);
    }  
 }
 
  private void setMouseReleasedMenuLabel(JLabel theReleasedLabel)
 {
    try
    {
        pressedSelected = false;
        if(theEnteredLabelReference.equals(thePressedLabelReference)==false)
        {
            if(thePressedLabelReference.equals(TheMenuLabelReference)==false)
            {
                thePressedLabelReference.setBackground(theCurrentMenuColor);
            }
            if(thePressedLabelReference.equals(TheMenuLabelReference)==true)
            {           
                thePressedLabelReference.setForeground(theTextMenuColor);
            }           
        }
        else
        {
            if(thePressedLabelReference.equals(TheMenuLabelReference)==true)
            {           
                thePressedLabelReference.setForeground(theTextMenuColor);
            }
        }
        
    }
    catch(Exception e)
    {
        System.out.println(e);
    }  
 } 
