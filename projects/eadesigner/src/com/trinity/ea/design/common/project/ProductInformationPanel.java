    private static synchronized Object[] getStringArraysFromString(String textArrayString)
    {
        ArrayList aryList = new ArrayList();
        String tempString = "";
        while(textArrayString.indexOf(",")!=-1)
        {
            tempString = textArrayString.substring(0,textArrayString.indexOf(","));
            textArrayString = textArrayString.substring(textArrayString.indexOf(",") + 1);
            aryList.add(tempString);
        }
        aryList.add(textArrayString);
        return aryList.toArray();
    }        

    /** Return true on all data entry fields contain text data, false otherwise. */
    public boolean getDataEntryIsComplete()
    {
        try
        {
            if(CopyrightTextField.getText().equalsIgnoreCase("")==true)
            {
                return false;
            }
            else if(ProductNameTextField.getText().equalsIgnoreCase("")==true)
            {
                return false;
            }
            else if(VendorNameTextField.getText().equalsIgnoreCase("")==true)
            {
                return false;
            }            
            else if(ProductInformationTextField.getText().equalsIgnoreCase("")==true)
            {
                return false;
            }
            else if(ProductURLTextField.getText().equalsIgnoreCase("")==true)
            {
                return false;
            }            
            else if(VersionBox1TextField.getText().equalsIgnoreCase("")==true)
            {
                return false;
            }            
        }
        catch(Exception e)
        {
            return false;
        }
        return true;
    }
    
    /** If getDataEntryIsComplete()==false, call this method to retrieve the first incomplete data field entry String. Return "" on unknown. */
    public String getIncompleteDataEntryString()
    {
        try
        {
            if(VendorNameTextField.getText().equalsIgnoreCase("")==true)
            {
                return "Please enter the vendor name (company, organization, or developer name).";
            }                
            else if(ProductNameTextField.getText().equalsIgnoreCase("")==true)
            {
                return "Please enter the product name (software title).";
            }
            else if(VersionBox1TextField.getText().equalsIgnoreCase("")==true)
            {
                return "Please enter the version (release version).";
            }          
            else if(CopyrightTextField.getText().equalsIgnoreCase("")==true)
            {
                return "Please enter the software product copyright date.";
            }             
            else if(ProductURLTextField.getText().equalsIgnoreCase("")==true)
            {
                return "Please enter the product URL (product web page address starting http://).";
            }       
            else if(ProductInformationTextField.getText().equalsIgnoreCase("")==true)
            {
                return "Please enter the product information URL (product information web page address starting http://).";
            }            
        }
        catch(Exception e)
        {
            return "";
        }
        return "";
    }
    
