    public synchronized void setProjectData()
    {
        try
        {
            CopyrightTextField.setText(ProjectManager.get("product_copyright"));
            // Start Set the Product Version Info...
            Object[] versionArray = getStringArraysFromString(ProjectManager.get("product_version"));
            VersionBox1TextField.setText((String)versionArray[0]);
            VersionBox2TextField.setText((String)versionArray[1]);
            VersionBox3TextField.setText((String)versionArray[2]);
            VersionBox4TextField.setText((String)versionArray[3]);
            // End Set the Product Version Info...          
            VendorNameTextField.setText(ProjectManager.get("product_vendor_name"));
            ProductNameTextField.setText(ProjectManager.get("product_name"));    
            ProductInformationTextField.setText(ProjectManager.get("product_info_url"));
            ProductURLTextField.setText(ProjectManager.get("product_url"));
                      
            CopyrightTextField.setCaretPosition(0);
            ProductNameTextField.setCaretPosition(0);
            VendorNameTextField.setCaretPosition(0);
            VersionBox1TextField.setCaretPosition(0);
            VersionBox2TextField.setCaretPosition(0);
            VersionBox3TextField.setCaretPosition(0);
            VersionBox4TextField.setCaretPosition(0);
            ProductInformationTextField.setCaretPosition(0);
            ProductURLTextField.setCaretPosition(0);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }

    /* Updates the project data properties changes */
    public void getDataUpdate()
    {
        try
        {
            ProjectManager.put("product_vendor_name", VendorNameTextField.getText());
            ProjectManager.put("product_name", ProductNameTextField.getText());    
            ProjectManager.put("product_version", VersionBox1TextField.getText() + "," + VersionBox2TextField.getText() + "," + VersionBox3TextField.getText() + "," + VersionBox4TextField.getText());
            ProjectManager.put("product_copyright", CopyrightTextField.getText());  
            ProjectManager.put("product_info_url", ProductInformationTextField.getText());
            ProjectManager.put("product_url", ProductURLTextField.getText());
            ProjectManager.saveTempNow();         
            // project_last_built is set by the Build Panel, once a build is complete.
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }    
            
