    public synchronized void getDataUpdate()
    {
	try
        {
            ProjectManager.putTempNoFileWrite("informationTextTitle", tfStartWindowTitleText.getText());            
            ProjectManager.putTempNoFileWrite("informationText", tfStartWindowText.getText().replaceAll("\n", "\\\\r\\\\n"));            
            ProjectManager.saveTempNow(); 
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
 
    public synchronized void setProjectData()
    {
        try
        {
             if(ProjectManager.get("informationTextTitle")!=null)
             {
                tfStartWindowTitleText.setText(ProjectManager.get("informationTextTitle"));
             }            
             if(ProjectManager.get("informationText")!=null)
             {
                tfStartWindowText.setText(ProjectManager.get("informationText").replaceAll("\\\\r\\\\n", "\n"));
             }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }    

    
