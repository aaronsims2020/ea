    public synchronized void getDataUpdate()
    {
    try
    {
        // File Locations
	try
	{
		if(tfSplashImage.getText().equalsIgnoreCase("")==false)
		{
			ProjectManager.putTempNoFileWrite("msgHeaderImgPath", new File(tfSplashImage.getText()).toURL().toString());
		}
		else
		{
			ProjectManager.putTempNoFileWrite("msgHeaderImgPath", "");
		}
	}
	catch(Exception ee)
	{
		ee.printStackTrace();
	}
// End File Locations

            ProjectManager.saveTempNow(); 
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
 
    public synchronized void setProjectData()
    {
        try
        {
// File Locations
	try
	{
            if(ProjectManager.get("msgHeaderImgPath")!=null)
            {
                    if(ProjectManager.get("msgHeaderImgPath").equalsIgnoreCase("")==false)
                    {
                        try
                        {
                            tfSplashImage.setText(new File(new URL(ProjectManager.get("msgHeaderImgPath")).getFile()).getCanonicalPath());
                        }
                        catch(Exception e)
                        {
                            
                        }
                            tfSplashImage.setCaretPosition(0);
                    }
                    else
                    {
                            tfSplashImage.setText("");
                    }
            }
            else
            {
                    tfSplashImage.setText("");
            }
	}
	catch(Exception e)
	{
		e.printStackTrace();
	}
// End File Locations


        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }    

    
