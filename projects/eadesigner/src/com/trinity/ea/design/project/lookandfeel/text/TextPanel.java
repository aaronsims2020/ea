    public synchronized void getDataUpdate()
    {
	try
        {
             ProjectManager.putTempNoFileWrite("startWindowTitle", tfStartWindowText.getText());            
             ProjectManager.putTempNoFileWrite("imageMissingText", tfImageMissingText.getText());
             ProjectManager.putTempNoFileWrite("progressBarDaysLeftText", tfDaysLeftText.getText());
             ProjectManager.putTempNoFileWrite("progressBarExpiredText", tfExpiredText.getText());
             ProjectManager.saveTempNow(); 
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
 
    public synchronized void setProjectData()
    {
        try
        {
             if(ProjectManager.get("startWindowTitle")!=null)
             {
                tfStartWindowText.setText(ProjectManager.get("startWindowTitle"));
             }
             if(ProjectManager.get("progressBarDaysLeftText")!=null)
             {
                tfDaysLeftText.setText(ProjectManager.get("progressBarDaysLeftText"));
             }            
             if(ProjectManager.get("progressBarExpiredText")!=null)
             {
                tfExpiredText.setText(ProjectManager.get("progressBarExpiredText"));
             }
             if(ProjectManager.get("imageMissingText")!=null)
             {
                tfImageMissingText.setText(ProjectManager.get("imageMissingText"));
             }            
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }    

    
