    private void rbOtherLandFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_rbOtherLandFActionPerformed
        if(rbOtherLandF.isSelected()==true)
        {
            rbSystemDefault.setSelected(false);
            lLandFClassName.setEnabled(true);
            tfLookAndFeelClassName.setEnabled(true);
             if(ProjectManager.get("LAndFPackage")!=null)
            {
                if(ProjectManager.get("LAndFPackage").equalsIgnoreCase("")==true)
                {
                     tfLookAndFeelClassName.setText("javax.swing.plaf.metal.MetalLookAndFeel");          
           
                }
                else if(ProjectManager.get("LAndFPackage").equalsIgnoreCase("system")==true)
                {
                    tfLookAndFeelClassName.setText("javax.swing.plaf.metal.MetalLookAndFeel");
                }
            }
            else
            {
                 tfLookAndFeelClassName.setText("javax.swing.plaf.metal.MetalLookAndFeel");                 
            }           
        }
        else
        {
            rbSystemDefault.setSelected(true);  
            lLandFClassName.setEnabled(false);  
            tfLookAndFeelClassName.setEnabled(false);           
        }
    }//GEN-LAST:event_rbOtherLandFActionPerformed

