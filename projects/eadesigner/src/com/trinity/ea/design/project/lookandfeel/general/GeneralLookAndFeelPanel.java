    public synchronized void getDataUpdate()
    {
	  try
          {
              if(rbOtherLandF.isSelected()==true)
              {    
                  if(tfLookAndFeelClassName.getText().equalsIgnoreCase("")==true)
                  {
                       ProjectManager.putTempNoFileWrite("LAndFPackage", "javax.swing.plaf.metal.MetalLookAndFeel");
                  }
                  else
                  {
                       ProjectManager.putTempNoFileWrite("LAndFPackage", tfLookAndFeelClassName.getText());
                  }
              }
              else
              {
                  ProjectManager.putTempNoFileWrite("LAndFPackage", "system");
              }
              if(VerticalButtonBarModel.isSelected()==true)
              {
                  ProjectManager.putTempNoFileWrite("project_gui_model", "0");
              }
              else 
              {
                  ProjectManager.putTempNoFileWrite("project_gui_model", "1");          
              }              
              
              ProjectManager.saveTempNow();
          }
          catch(Exception e)
          {
            e.printStackTrace();
          }
    }
    
    public synchronized void setProjectData()
    {
        try
        {
            if(ProjectManager.get("LAndFPackage")!=null)
            {
                if(ProjectManager.get("LAndFPackage").equalsIgnoreCase("system")==true)
                {
                    rbSystemDefault.setSelected(true);
                    rbOtherLandF.setSelected(false);
                    lLandFClassName.setEnabled(false);
                    tfLookAndFeelClassName.setEnabled(false);           
           
                }
                else
                {
                    rbSystemDefault.setSelected(false);
                    rbOtherLandF.setSelected(true);   
                    lLandFClassName.setEnabled(true);
                    tfLookAndFeelClassName.setEnabled(true);   
                    tfLookAndFeelClassName.setText(ProjectManager.get("LAndFPackage"));
                }
            }
            else
            {
                rbSystemDefault.setSelected(true);
                rbOtherLandF.setSelected(false);
                lLandFClassName.setEnabled(false);
                tfLookAndFeelClassName.setEnabled(false);                   
            }
            
            if(ProjectManager.get("project_gui_model")!=null)
            {
                if(ProjectManager.get("project_gui_model").equalsIgnoreCase("0")==true)
                {
                    VerticalButtonBarModel.setSelected(true);
                    HorizontalButtonBarModel.setSelected(false);           
                }
                else if(ProjectManager.get("project_gui_model").equalsIgnoreCase("1")==true)
                {
                    VerticalButtonBarModel.setSelected(false);
                    HorizontalButtonBarModel.setSelected(true);
                }
                else
                {
                    VerticalButtonBarModel.setSelected(true);
                    HorizontalButtonBarModel.setSelected(false);                    
                }
            }
            else
            {
                    VerticalButtonBarModel.setSelected(true);
                    HorizontalButtonBarModel.setSelected(false);                     
            }            
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
    
