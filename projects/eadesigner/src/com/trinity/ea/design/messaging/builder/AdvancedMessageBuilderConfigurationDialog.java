    public static int showLocaleSelectorDialog(int intMsgID, String strPwdSeed, int intPwdCount)
    {
        msgIDNumber = intMsgID;
        pwdSeed = strPwdSeed;
        pwdCount = intPwdCount;
        AdvancedMessageBuilderConfigurationDialog dlg = new AdvancedMessageBuilderConfigurationDialog(null,true);
	  dlg.show();
        pwdSeed = tfEncryptionPasswordSeed.getText();
        SpinnerNumberModel sm = (SpinnerNumberModel)spinEncryptionPasswordCount.getModel();
        pwdCount = sm.getNumber().intValue();
        sm = (SpinnerNumberModel)spinMessageID.getModel();
        msgIDNumber = sm.getNumber().intValue(); 
        dlg.dispose();
        return result;
    }
    
