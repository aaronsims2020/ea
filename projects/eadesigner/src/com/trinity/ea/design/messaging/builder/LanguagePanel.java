    public String getMessage()
    {
	try
	{
            String theText = taMessageBody.getText();
            String theText2 = theText.replaceAll("\r\n", "\\\\r\\\\n");     
            if(theText.equalsIgnoreCase(theText2)==true)
            {
                return theText2.replaceAll("\n", "\\\\r\\\\n");                
            }
            else
            {
                return theText2;
            }
	}
	catch(Exception e)
	{
            e.printStackTrace();
            return taMessageBody.getText();
	}        
    }       
 
