    public synchronized void setProjectData()
    {
        try
        {
		// Auto Update Support Functionality Checkbox Enabled/Disabled   
            if(ProjectManager.get("msgBaseURLChangesAllowed").equalsIgnoreCase("true")==true)
            {
                cbAllowURLChangesEnabled.setSelected(true);              
            }
            else
            {
                cbAllowURLChangesEnabled.setSelected(false);               
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
  
    public synchronized void getDataUpdate()
    {
	try
	{
		// Auto Update Support Functionality Checkbox Enabled/Disabled
            if(cbAllowURLChangesEnabled.isSelected()==true)
            {
		    ProjectManager.putTempNoFileWrite("msgBaseURLChangesAllowed", "true");
            }
            else
            {
		    ProjectManager.putTempNoFileWrite("msgBaseURLChangesAllowed", "false");
            }
            ProjectManager.saveTempNow();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
 
