    private void SpinnerPanelMouseExited(java.awt.event.MouseEvent evt) {
	  getDataUpdate();
        if(((EAPropertiesPanel)getParentComponent()).getEAListItemsArrayLength()<Integer.valueOf(ProjectManager.get("msgMaxBaseURLs")).intValue())
	  {
		 ((EAPropertiesPanel)getParentComponent()).setMaxEAListItems(Integer.valueOf(ProjectManager.get("msgMaxBaseURLs")).intValue());
		 ((EAPropertiesPanel)getParentComponent()).setAddButtonEnabled(true);
	  }
	  else
	  {
		 ((EAPropertiesPanel)getParentComponent()).setMaxEAListItems(Integer.valueOf(ProjectManager.get("msgMaxBaseURLs")).intValue());
		 ((EAPropertiesPanel)getParentComponent()).setAddButtonEnabled(false);
	  }
    }

