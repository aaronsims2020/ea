    public synchronized void setProjectData()
    {
        try
        {
            // Auto Update Maximum URLs JSpinner value
            if(ProjectManager.get("msgMaxBaseURLs")!=null)
            {
		    maxURLsSpinner.setOpaque(true);
   		    maxURLsSpinner.setModel(new SpinnerNumberModel(Integer.valueOf(ProjectManager.get("msgMaxBaseURLs")),new Integer(1),new Integer(100),new Integer(1)));
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
  
    public synchronized void getDataUpdate()
    {
	try
	{
            // Auto Update Maximum URLs JSpinner value
            SpinnerNumberModel sm = (SpinnerNumberModel)maxURLsSpinner.getModel();
            ProjectManager.putTempNoFileWrite("msgMaxBaseURLs", String.valueOf(sm.getNumber().intValue()));            
            ProjectManager.saveTempNow();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }    

