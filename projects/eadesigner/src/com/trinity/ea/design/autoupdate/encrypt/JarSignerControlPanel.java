   public synchronized void getDataUpdate()
   {
        try
        {
                if(rbX500.isSelected()==true)
                {
                    ProjectManager.putTempNoFileWrite("autoUpdateX509CertificateEnabled", "false");
                }
                else
                {
                    ProjectManager.putTempNoFileWrite("autoUpdateX509CertificateEnabled", "true");
                }
		    X500Panel.getDataUpdate();
		    X509Panel.getDataUpdate();
                ProjectManager.saveTempNow();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
  }   

    public synchronized void setProjectData()
    {
        try
        {
            if(ProjectManager.get("autoUpdateX509CertificateEnabled")!=null)
            {
                if(ProjectManager.get("autoUpdateX509CertificateEnabled").equalsIgnoreCase("true")==true)
                {
                    rbX500.setSelected(false);
                    rbX509.setSelected(true);
                }
                else
                {
                    rbX500.setSelected(true); 
                    rbX509.setSelected(false);
                }
            }
            else
            {
                rbX500.setSelected(true); 
                rbX509.setSelected(false);                
            }
		X500Panel.setProjectData();
		X509Panel.setProjectData();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }

