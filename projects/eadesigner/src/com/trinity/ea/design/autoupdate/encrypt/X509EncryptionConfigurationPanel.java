   public synchronized void getDataUpdate()
   {
        try
        {
		// Auto Update Support Password Seed and Password Count Functionality variables
		ProjectManager.putTempNoFileWrite("autoUpdateX509Certificate", taX509Certificate.getText());
		ProjectManager.saveTempNow();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
  }   

    public synchronized void setProjectData()
    {
        try
        {
		// Registration Code Serial Number TextField  
            if(ProjectManager.get("autoUpdateX509Certificate")!=null)
            {
		    taX509Certificate.setOpaque(true);
		    taX509Certificate.setText(ProjectManager.get("autoUpdateX509Certificate"));
		    taX509Certificate.setCaretPosition(0);
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }

