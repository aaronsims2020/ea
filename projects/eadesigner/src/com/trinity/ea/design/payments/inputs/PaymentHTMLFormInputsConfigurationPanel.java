   public synchronized void getDataUpdate()
   {
	try
	{
		// Optin Support Functionality Checkbox Enabled/Disabled
		// Refund Policy Checkbox Enabled/Disabled
            if(cbEnableRefundPolicy.isSelected()==true)
            {
		    ProjectManager.putTempNoFileWrite("refundPolicyEnabled", "true");
            }
            else
            {
		    ProjectManager.putTempNoFileWrite("refundPolicyEnabled", "false");
            }
		
		ProjectManager.putTempNoFileWrite("formActionURL", tfPaymentFormActionURL.getText());

            if(paymentRadioPostRequestMethod.isSelected()==true)
            {
			ProjectManager.putTempNoFileWrite("formRequestMethod", "POST");
		}
		else
		{
			ProjectManager.putTempNoFileWrite("formRequestMethod", "GET");
		}
		ProjectManager.saveTempNow();
		//TheContentPanelReference.getDataUpdate();
	}
	catch(Exception e)
	{
		e.printStackTrace();
	}
  }   

    public synchronized void setProjectData()
    {
        try
        {
		// Optin Support Functionality Checkbox Enabled/Disabled   
		// Refund Policy Checkbox Enabled/Disabled
            if(ProjectManager.get("refundPolicyEnabled").equalsIgnoreCase("true")==true)
            {
		    cbEnableRefundPolicy.setSelected(true);
            }
            else
            {
		    cbEnableRefundPolicy.setSelected(false);
            }
            if(ProjectManager.get("formRequestMethod").equalsIgnoreCase("POST")==true)
            {
			paymentRadioPostRequestMethod.setSelected(true);
			paymentRadioGetRequestMethod.setSelected(false);
		}
		else
		{
			paymentRadioGetRequestMethod.setSelected(true);
			paymentRadioPostRequestMethod.setSelected(false);
		}
		tfPaymentFormActionURL.setOpaque(true);
		tfPaymentFormActionURL.setText(ProjectManager.get("formActionURL"));
		tfPaymentFormActionURL.setCaretPosition(0);
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }

    
