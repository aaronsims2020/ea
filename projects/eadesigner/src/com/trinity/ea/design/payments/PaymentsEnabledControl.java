    public synchronized void getDataUpdate()
    {
	try
	{
		// Registration Support Functionality Checkbox Enabled/Disabled
            if(cbPaymentProcessingEnabled.isSelected()==true)
            {
		    ProjectManager.putTempNoFileWrite("paymentProcessingSupportEnabled", "true");
 		    ProjectManager.putTempNoFileWrite("project_payment_processing_support_is_enabled", "true");                            
            }
            else
            {
		    ProjectManager.putTempNoFileWrite("paymentProcessingSupportEnabled", "false");
		    ProjectManager.putTempNoFileWrite("project_payment_processing_support_is_enabled", "false");               
            }
            ProjectManager.saveTempNow();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
 
    public synchronized void setProjectData()
    {
        try
        {
		// Optin Support Functionality Checkbox Enabled/Disabled   
            if(ProjectManager.get("project_payment_processing_support_is_enabled").equalsIgnoreCase("true")==true)
            {
                cbPaymentProcessingEnabled.setSelected(true);              
            }
            else
            {
                cbPaymentProcessingEnabled.setSelected(false);               
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }

