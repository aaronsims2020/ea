    public void getDataUpdate()
    {
        try
        {
            ProjectManager.putTempNoFileWrite("optinFormActionURL", "http://www.getresponse.com/cgi-bin/add.cgi");
            ProjectManager.putTempNoFileWrite("optinFormRequestMethod", "POST");
            ProjectManager.putTempNoFileWrite("optinInputFullName", "category2");
            ProjectManager.putTempNoFileWrite("optinInputEMailAddress", "category3"); 
            //::: represents a comma in the Rules Properties File
            String getresptracking = "777";
		String miscInfo = "misc_info";
		SpinnerNumberModel sm = (SpinnerNumberModel)spinMessageNumber.getModel();
            getresptracking = String.valueOf(sm.getNumber().intValue());
            if(radioFollowUpMessagesYes.isSelected()==true)
            {
			miscInfo = tfAdCatagory.getText();
            }
		else if(radioFollowUpMessagesNo.isSelected()==true)
		{
			miscInfo = "misc_info";
		}
            if(radioAdTrackingNo.isSelected()==false)
            {
                ProjectManager.putTempNoFileWrite("optinInputHiddenNames", "category1,confirmation,error_page,misc,ref");
                ProjectManager.putTempNoFileWrite("optinInputHiddenValues",tfAutoResponderListName.getText() + ",,," + miscInfo + "," + getresptracking);                
            }        
            else if(radioAdTrackingNo.isSelected()==true)
            {
                ProjectManager.putTempNoFileWrite("optinInputHiddenNames", "category1,confirmation,error_page,misc,ref");
                ProjectManager.putTempNoFileWrite("optinInputHiddenValues",tfAutoResponderListName.getText() + ",,," + miscInfo + ",777");
            }
            ProjectManager.saveTempNow();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }

    public void setProjectData()
    {
        Object[] inputNames = getStringArraysFromString(ProjectManager.get("optinInputHiddenNames"));
        Object[] inputValues = getStringArraysFromString(ProjectManager.get("optinInputHiddenValues"));
        HashMap theTempMap = new HashMap(inputNames.length);
        for(int i = 0;i<inputNames.length;i++)
        {
            theTempMap.put((String)inputNames[i], new Integer(i));
        }
        try
        {
           int intCategory1 = -1;
           int intConfirmation = -1;
           int intErrorPage = -1;
           int intMisc = -1;
           int intTracking = -1;
           boolean boolAdTracking = false;
           String strTrackerNumber = "1";
           if(theTempMap.get("category1")!=null)
           {
                intCategory1 = ((Integer)theTempMap.get("category1")).intValue();
           }
           if(theTempMap.get("confirmation")!=null)
           {
                intConfirmation = ((Integer)theTempMap.get("confirmation")).intValue();
           }
           if(theTempMap.get("error_page")!=null)
           {
                intErrorPage = ((Integer)theTempMap.get("error_page")).intValue();              
           } 
           if(theTempMap.get("misc")!=null)
           {
                intMisc = ((Integer)theTempMap.get("misc")).intValue();              
           } 
           if(theTempMap.get("ref")!=null)
           {
                intTracking = ((Integer)theTempMap.get("ref")).intValue();
           }
               if(intTracking != -1)
               {
                   String theRefString = (String)inputValues[intTracking];
                   if(theRefString.startsWith("00")==true)
                   {
                      theRefString = theRefString.substring(2,2);
                   }
                   else if(theRefString.startsWith("0")==true)
                   {
                      theRefString = theRefString.substring(1,2);                       
                   }                
                   
                   if(theRefString.equalsIgnoreCase("777")==true)
                   {
                    try
                    {
				if(radioAdTrackingYes.isSelected()==true)
				{
                       		radioAdTrackingYes.setSelected(true);
		       		spinMessageNumber.setModel(new SpinnerNumberModel(new Integer(777),new Integer(1),new Integer(999),new Integer(1)));
                       		radioAdTrackingNo.setSelected(false);                  
                       		expandAdTracking();
				}
				else if(radioAdTrackingNo.isSelected()==true)
				{
                       		radioAdTrackingYes.setSelected(false);
		       		spinMessageNumber.setModel(new SpinnerNumberModel(new Integer(777),new Integer(1),new Integer(999),new Integer(1)));
                       		radioAdTrackingNo.setSelected(true);                  
                       		collapseAdTracking();
				}
				else
				{
                       		radioAdTrackingYes.setSelected(false);
		       		spinMessageNumber.setModel(new SpinnerNumberModel(new Integer(777),new Integer(1),new Integer(999),new Integer(1)));
                       		radioAdTrackingNo.setSelected(true);                  
                       		collapseAdTracking();
			      }           
             	  }          
                    catch(Exception e)
                    {
                        e.printStackTrace();
                    } 
                   }
                   else
                   {
                        if(Integer.valueOf(theRefString).intValue() < 1000)
                        {		     	  
                                spinMessageNumber.setModel(new SpinnerNumberModel(Integer.valueOf(theRefString),new Integer(1),new Integer(999),new Integer(1)));
                        }
                        else
                        {
                                spinMessageNumber.setModel(new SpinnerNumberModel(Integer.valueOf(theRefString),new Integer(1),Integer.valueOf(strTrackerNumber),new Integer(1)));
                        }
                       radioAdTrackingYes.setSelected(true); 
                       radioAdTrackingNo.setSelected(false);                   
                       expandAdTracking();
                   }

               if(intCategory1!=-1)
               {
                    tfAutoResponderListName.setText((String)inputValues[intCategory1]);
               }
               if(intMisc != -1)
               {
                    tfAdCatagory.setText((String)inputValues[intMisc]);
               }
           }
           else
           {
               spinMessageNumber.setModel(new SpinnerNumberModel(new Integer(777),new Integer(1),new Integer(999),new Integer(1)));
               radioAdTrackingYes.setSelected(false);
               radioAdTrackingNo.setSelected(true);                      
               collapseAdTracking();                  
               if(intCategory1 != -1)
               {
                   tfAutoResponderListName.setText((String)inputValues[intCategory1]);
               }
            }
            if(intMisc != -1)
            {
			if(((String)inputValues[intMisc]).equalsIgnoreCase("misc_info")==true)
			{
                    try
                    {
				if(radioFollowUpMessagesYes.isSelected()==true)
				{
                       		radioFollowUpMessagesYes.setSelected(true); 
                       		radioFollowUpMessagesNo.setSelected(false);                    
                       		expandAutoResponse();
				}
				else if(radioFollowUpMessagesNo.isSelected()==true)
				{
                       		radioFollowUpMessagesYes.setSelected(false); 
                       		radioFollowUpMessagesNo.setSelected(true);                    
                       		collapseAutoResponse();
				}
				else
				{
                       		radioFollowUpMessagesYes.setSelected(false); 
                       		radioFollowUpMessagesNo.setSelected(true);                    
                       		collapseAutoResponse();
				}
            	    }          
                    catch(Exception e)
                    {
                        e.printStackTrace();
                    }
			}
			else
			{
                       radioFollowUpMessagesYes.setSelected(true); 
                       radioFollowUpMessagesNo.setSelected(false);                    
                       expandAutoResponse();
			}

			
			tfAdCatagory.setText((String)inputValues[intMisc]);
			  
            }
            else
            {
                    try
                    {
				if(radioFollowUpMessagesYes.isSelected()==true)
				{
                       		radioFollowUpMessagesYes.setSelected(true); 
                       		radioFollowUpMessagesNo.setSelected(false);                    
                       		expandAutoResponse();
                       		tfAdCatagory.setText("misc_info");
				}
				else if(radioFollowUpMessagesNo.isSelected()==true)
				{
                       		radioFollowUpMessagesYes.setSelected(false); 
                       		radioFollowUpMessagesNo.setSelected(true);                    
                       		collapseAutoResponse();
                       		tfAdCatagory.setText("misc_info");
				}
				else
				{
                       		radioFollowUpMessagesYes.setSelected(false); 
                       		radioFollowUpMessagesNo.setSelected(true);                    
                       		collapseAutoResponse();
                       		tfAdCatagory.setText("misc_info");
				}
            	    }          
                    catch(Exception e)
                    {
                        e.printStackTrace();
                    }
        	}
	  }
        catch(Exception e)
        {
             e.printStackTrace();
        }
    }
    
