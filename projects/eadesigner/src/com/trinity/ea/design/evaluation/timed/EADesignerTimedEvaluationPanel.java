    public void getDataUpdate()
    {
        try
        {
            // TODO: add error handling code for non numeric entry handling
            final long day = 86400000;
            final long hour = 3600000;
            final long minute = 60000; 
            final long second = 1000;
            ProjectManager.putTempNoFileWrite("runtimeMillasecondsToExpire", String.valueOf(Long.valueOf(tfEvaluationDays.getText()).longValue() * day + Long.valueOf(tfEvaluationHours.getText()).longValue() * hour + Long.valueOf(tfEvaluationMinutes.getText()).longValue() * minute + Long.valueOf(tfEvaluationSeconds.getText()).longValue() * second));
         
            // TODO: Add Actions, and Custom Action to Evaluation GUI, until then use the defaults...
            if(EvaluationExpiredDisableExpiredSoftware.isSelected()==true)
            {
                ProjectManager.putTempNoFileWrite("evaluation_disable_expired_software_enabled", "true");
                //ProjectManager.putTempNoFileWrite("expiredAction", "com.trinity.ea.actions.ExpiredAction");               
            }
            else
            {
                ProjectManager.putTempNoFileWrite("evaluation_disable_expired_software_enabled", "false");
                //ProjectManager.putTempNoFileWrite("expiredAction", ProjectManager.get("notExpiredAction"));               
            }
            ProjectManager.saveTempNow();          
        }
        catch(NumberFormatException e)
        {
            // TODO: NumberFOrmatException Action goes here...
            e.printStackTrace();
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }     
    
    public synchronized void setProjectData()
    {
        try
        {
            String[] timeToExpiration =  DaysHoursMinutesSeconds.getDaysHoursMinutesSeconds(Long.valueOf(ProjectManager.get("runtimeMillasecondsToExpire")).longValue());
            tfEvaluationDays.setText(timeToExpiration[0]);
            tfEvaluationHours.setText(timeToExpiration[1]);
            tfEvaluationMinutes.setText(timeToExpiration[2]);
            tfEvaluationSeconds.setText(timeToExpiration[3]);
            tfEvaluationDays.setCaretPosition(0);
            tfEvaluationHours.setCaretPosition(0);
            tfEvaluationMinutes.setCaretPosition(0);
            tfEvaluationSeconds.setCaretPosition(0);          
            if(ProjectManager.get("evaluation_disable_expired_software_enabled").equalsIgnoreCase("true")==true)
            {
                EvaluationExpiredDisableExpiredSoftware.setSelected(true);              
            }
            else
            {
                EvaluationExpiredDisableExpiredSoftware.setSelected(false);               
            }

        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }
