    public void setKeyManager()
    { 
     KeyboardFocusManager.getCurrentKeyboardFocusManager().addKeyEventDispatcher(
        new KeyEventDispatcher() {
            public boolean dispatchKeyEvent(KeyEvent e) 
            {
               
              if (e.getID() == KeyEvent.KEY_PRESSED) 
              {
                   if (e.isAltDown() == true || e.isMetaDown() == true || e.isControlDown()==true) 
                   {   
				armed = true;
			 }
              }            
                // This example converts all typed keys to upper case
              if (e.getID() == KeyEvent.KEY_RELEASED) 
              {
			if(armed==true)
			{
                        if(e.getComponent().equals(UseEvaluationImageButton)==true)
                        {
				    if(e.getKeyCode()==UseEvaluationImageButton.getDisplayedMnemonic())
                            {
					getUseEvaluationAction();
				    }
                        }
                        if(e.getComponent().equals(BuyNowImageButton)==true)
                        {
					if(e.getKeyCode()==BuyNowImageButton.getDisplayedMnemonic())
                        	{
						getBuyNowAction();
					}
                        }
                        if(e.getComponent().equals(ActivateImageButton)==true)
                        {
					if(e.getKeyCode()==ActivateImageButton.getDisplayedMnemonic())
                        	{
						getActivateAction();
					}
                        }
                        if(e.getComponent().equals(InformationImageButton)==true)
                        {
					if(e.getKeyCode()==InformationImageButton.getDisplayedMnemonic())
                        	{
						getInfoAction();
					}
                        }
                        if(e.getComponent().equals(ExitImageButton)==true)
                        {
					if(e.getKeyCode()==ExitImageButton.getDisplayedMnemonic())
                        	{
						getExitAction();
					}
                        }
				armed = false;
			}
              }
              // If the key should not be dispatched to the
              // focused component, set discardEvent to true
              boolean discardEvent = false;
              return discardEvent;
            }
        });       
    }

